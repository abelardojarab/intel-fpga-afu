// altera message_off 10036 10236
	
`timescale 1 ps / 1 ps
module alt_aeu_40_pfc_rx_counter (
	input wire reset_n, 
	input wire clk, 
	input wire ena, 
	input wire clr, 
	output reg[31:0] count);

	always @ (posedge clk or negedge reset_n) begin
		if (~reset_n) count <= 32'd0; else if (clr) count <= 32'd0;
		else if (ena) count <= count+1;
	end
endmodule
